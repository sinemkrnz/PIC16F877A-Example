    LIST P=16F877A
    #INCLUDE <P16F877A.INC>
    VAR1 EQU 0X20
    VAR2 EQU 0X21
    COUNTER EQU 0X22
    ORG 0X00
    GOTO INIT
    
INIT
    BSF STATUS,5
    CLRF TRISD
    BCF STATUS,5
    MOVLW D'16'
    MOVWF VAR1
    MOVLW D'5'
    MOVWF VAR2
    CLRF STATUS
    CLRF COUNTER
DIVID
    INCF COUNTER
    MOVF VAR2,W
    SUBWF VAR1,F
    BTFSC STATUS,0
    GOTO DIVID
    GOTO RESULT
    
RESULT
    DECF COUNTER
    MOVF COUNTER,W
    MOVWF PORTD

    
END 


