    LIST P=16F877A
    #INCLUDE <P16F877A.INC>
    
    VAR1 EQU 0X20
    VAR2 EQU 0X21
    ORG 0X00
    GOTO INIT
    
INIT
    BSF STATUS,5
    MOVLW B'11111111'
    MOVWF TRISB
    MOVWF TRISC
    CLRF PORTD
    BCF STATUS,5
    
MAIN
    MOVF PORTB,W
    MOVWF VAR1
    MOVF PORTC,W
    MOVWF VAR2
    MOVF VAR1,W
    ADDWF VAR2,W
    MOVWF PORTD
    GOTO MAIN
END
    
 
 


